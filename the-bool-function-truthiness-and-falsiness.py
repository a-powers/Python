### negatives and positive numbers are True; the zero is False
### an empty string is False

if 3:
    print("Hello")

if -1:
    print("Goodbye")

if 0:
    print("Will this print?")

if "hello":
    print("La la la")

if "":
    print("This will not print")

print(bool(1))
print(bool(0))
print(bool(-1))
print(bool(""))
print(bool("python"))
print(bool(3.14))
print(bool(0.0))